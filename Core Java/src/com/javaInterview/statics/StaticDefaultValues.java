package com.javaInterview.statics;

public class StaticDefaultValues {

	// static variables get default values
	static byte b;
	static short s;
	static int i;
	static long l;

	static boolean boo;

	static float fl;
	static double d;

	static String str;

	public static void main(String[] args) {

		System.out.println("static byte = " + b);
		System.out.println("static short = " + s);
		System.out.println("static int = " + i);
		System.out.println("static long = " + l);
		System.out.println("static booloan = " + boo);
		System.out.println("static float = " + fl);
		System.out.println("static double = " + d);

		System.out.println("static String = " + str);
	}

}
